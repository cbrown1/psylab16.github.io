   <div id="mainblock">
        My research focuses on the effects of hearing loss on consonant perception and 
        how it varies with vowel context. In continuous speech, consonants that are 
        represented by fast-changing weak energies are often embedded in vowel sounds 
        (such as a vowel-consonant-vowel syllable) that are more intense and longer. 
        Therefore, the audibility of the acoustic changes used for consonant 
        identification may be reduced by a neighbor vowel because of the temporal and 
        spectral characteristics of masking. Such masking can be detrimental for 
        listeners with hearing impairment due to their reduced frequency and temporal 
        resolution. The long-term goal of my research is to delineate implications for 
        the design of hearing aid algorithms, such as adjusting the gain of a weak 
        sound according to the spectral components of its neighboring sound.
